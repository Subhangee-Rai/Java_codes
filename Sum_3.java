public class Sum_3 {
    public static void main(String[] args) {
        int num1=10,num2=20,num3=30;
        System.out.println("Sum of integer numbers is: "+(num1+num2+num3));
        float f1=10.1f, f2=10.2f;
        double f3=10.3; //double can be declared without using d after the value while f is needed for float.
        System.out.println("\nSum of floating numbers is: "+(f1+f2+f3));

    }
}
